# category_summary() → for each category: count, avg price, avg rating (use defaultdict(list))
from collections import defaultdict

def category_summary():
    category_data = defaultdict(list)

    for product in catalog.values():
        category = product.get("category")
        category_data[category].append(product)

    result = {}

    for category, products in category_data.items():
        count = len(products)
        avg_price = sum(p.get("price", 0) for p in products) / count
        avg_rating = sum(p.get("rating", 0) for p in products) / count

        result[category] = {
            "count": count,
            "avg_price": avg_price,
            "avg_rating": avg_rating
        }

    return result

category_summary()

# apply_discount(category, percent) → reduce prices for a category (dict comprehension)
def apply_discount(category, percent):
    return {
        sku: {**product, "price": product.get("price", 0) * (1 - percent / 100)}
        if product.get("category") == category
        else product
        for sku, product in catalog.items()
    }
apply_discount("electronics", 10)

