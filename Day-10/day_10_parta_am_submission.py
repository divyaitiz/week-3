# price_range(min_price, max_price) → filter products by price range
def price_range(min_price, max_price):
    price_result = {}

    for sku, product in catalog.items():
        price = product.get("price")
        if min_price <= price <= max_price:
            price_result[sku] = product.get("name")

    return price_result

